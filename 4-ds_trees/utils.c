#include "tree.h"


/**
* _max- returns maximum int
* @a: first integer
* @b: second integer

* Return: maximum of the a and b
*/
int _max(int a, int b)
{
    if (a >= b)
        return (a);
    return (b);
}

/**
* _min- returns maximum int
* @a: first integer
* @b: second integer

* Return: minimum of the a and b
*/
int _min(int a, int b)
{
    if (a <= b)
        return (a);
    return (b);
}